#!/usr/bin/env node
// local-test-setup.mjs — set up two parallel "developer" environments on this
// one machine so you can drive a real call between two Claude Code sessions.
//
// Creates ~/peerd-local-test/{alice,bob}/ each with:
//   .claude/
//     peerd/  (its own state dir, port, control socket, TLS cert)
//     settings.local.json  (MCP server + hooks + status line, project-scoped)
//     skills/<name> -> symlinks to repo's skills/
//
// Then you open four terminals: alice's peerd, bob's peerd, alice's claude, bob's claude.
//
// Safe to re-run; recreates configs but reuses TLS certs if present.

import * as fs from "node:fs";
import * as path from "node:path";
import * as os from "node:os";
import { fileURLToPath } from "node:url";
import { spawnSync } from "node:child_process";
import * as crypto from "node:crypto";

const __filename = fileURLToPath(import.meta.url);
const REPO_ROOT = path.dirname(__filename);

const TEST_BASE = path.join(os.homedir(), "peerd-local-test");
const SIDES = [
  { name: "alice", port: 7787, peerName: "bob",   peerPort: 7788 },
  { name: "bob",   port: 7788, peerName: "alice", peerPort: 7787 },
];

const log = (...a) => console.log("[local-test]", ...a);

function ensureBuilt() {
  const required = [
    path.join(REPO_ROOT, "peerd", "dist", "tls.js"),
    path.join(REPO_ROOT, "peer-mcp", "dist", "index.js"),
    path.join(REPO_ROOT, "peer-mcp", "dist", "check_inbox.js"),
    path.join(REPO_ROOT, "peer-mcp", "dist", "status_line.js"),
  ];
  if (required.every((p) => fs.existsSync(p))) return;
  log("running npm run build...");
  const r = spawnSync("npm", ["run", "build"], { cwd: REPO_ROOT, stdio: "inherit" });
  if (r.status !== 0) {
    console.error("npm run build failed");
    process.exit(1);
  }
}

async function loadEnsureTls() {
  const mod = await import(path.join(REPO_ROOT, "peerd", "dist", "tls.js"));
  return mod.ensureTls;
}

function randomToken() {
  return "sk_peer_" + crypto.randomBytes(18).toString("hex");
}

function sideStateDir(name) {
  return path.join(TEST_BASE, name, ".claude", "peerd");
}

function sideClaudeDir(name) {
  return path.join(TEST_BASE, name, ".claude");
}

function sideRoot(name) {
  return path.join(TEST_BASE, name);
}

async function setupTls(ensureTls) {
  const out = {};
  for (const s of SIDES) {
    const dir = sideStateDir(s.name);
    fs.mkdirSync(dir, { recursive: true });
    const tls = await ensureTls(dir, s.name);
    out[s.name] = tls;
  }
  return out;
}

function writePeersToml(side, otherName, otherPort, ourToken, peerToken, peerFingerprint) {
  const lines = [
    `# auto-generated by local-test-setup.mjs (safe to re-edit)`,
    `self = "${side.name}"`,
    `port = ${side.port}`,
    ``,
    `[peers.${otherName}]`,
    `host          = "127.0.0.1"`,
    `port          = ${otherPort}`,
    `token         = "${ourToken}"`,
    `inbound_token = "${peerToken}"`,
    `fingerprint   = "${peerFingerprint}"`,
    ``,
  ];
  fs.writeFileSync(path.join(sideStateDir(side.name), "peers.toml"), lines.join("\n"));
}

function writeSettings(side) {
  const stateDir = sideStateDir(side.name);
  const sockPath = path.join(stateDir, "control.sock");
  const peerMcpEntry = path.join(REPO_ROOT, "peer-mcp", "dist", "index.js");
  const checkInbox  = path.join(REPO_ROOT, "peer-mcp", "dist", "check_inbox.js");
  const precheck    = path.join(REPO_ROOT, "peer-mcp", "dist", "precheck.js");
  const statusLine  = path.join(REPO_ROOT, "peer-mcp", "dist", "status_line.js");

  // 1. Project-scoped MCP server lives in .mcp.json (Claude Code reads this at project root).
  const mcpConfig = {
    mcpServers: {
      peerd: {
        command: "node",
        args: [peerMcpEntry],
        env: {
          PEERD_CONTROL_SOCK: sockPath,
        },
      },
    },
  };
  fs.writeFileSync(
    path.join(sideRoot(side.name), ".mcp.json"),
    JSON.stringify(mcpConfig, null, 2) + "\n",
  );

  // 2. Project-scoped hooks + status line + permissions live in .claude/settings.local.json.
  const settings = {
    // Auto-approve routine peerd tools so the user isn't asked permission
    // for every peer_send / peer_recv / peer_end during a call.
    //
    // INTENTIONALLY left as "ask" (i.e., NOT in the allow list):
    //   - peer_accept_invite  ← the native permission prompt IS the Accept UI
    //   - peer_deny_invite    ← same for Decline
    permissions: {
      allow: [
        // ALL peerd MCP tools auto-approved.
        // The user's accept/decline decision happens via AskUserQuestion (the
        // built-in arrow-key picker), NOT via tool-permission prompts. Without
        // this, calling peer_accept_invite would pop a separate Allow/Deny
        // confirm right after the user already picked Accept.
        "mcp__peerd__peer_invite",
        "mcp__peerd__peer_accept_invite",
        "mcp__peerd__peer_deny_invite",
        "mcp__peerd__peer_send",
        "mcp__peerd__peer_recv",
        "mcp__peerd__peer_end",
        "mcp__peerd__peer_list_inbox",
        "mcp__peerd__peer_list_peers",
        "mcp__peerd__peer_human_inject",
        // Skills.
        "Skill(call)",
        "Skill(accept)",
        "Skill(deny)",
        "Skill(end-call)",
        "Skill(action-items)",
      ],
    },
    hooks: {
      // UserPromptSubmit fires BEFORE the agent processes a prompt.
      // We use it to inject active-call context so the agent automatically
      // drives any in-flight call (e.g. one that was just ambient-accepted).
      UserPromptSubmit: [
        {
          matcher: "",
          hooks: [
            {
              type: "command",
              command: `PEERD_CONTROL_SOCK=${JSON.stringify(sockPath)} node ${JSON.stringify(precheck)}`,
              timeout: 5,
            },
          ],
        },
      ],
      // Stop fires after the agent finishes. We use it to put a notification
      // on screen for the user (systemMessage) when calls/invites are pending.
      Stop: [
        {
          matcher: "",
          hooks: [
            {
              type: "command",
              command: `PEERD_CONTROL_SOCK=${JSON.stringify(sockPath)} node ${JSON.stringify(checkInbox)}`,
              timeout: 5,
            },
          ],
        },
      ],
    },
    statusLine: {
      type: "command",
      command: `PEERD_CONTROL_SOCK=${JSON.stringify(sockPath)} node ${JSON.stringify(statusLine)}`,
      padding: 1,
      refreshInterval: 5,
    },
  };

  const claudeDir = sideClaudeDir(side.name);
  fs.mkdirSync(claudeDir, { recursive: true });
  fs.writeFileSync(
    path.join(claudeDir, "settings.local.json"),
    JSON.stringify(settings, null, 2) + "\n",
  );
}

function symlinkSkills(side) {
  const target = path.join(sideClaudeDir(side.name), "skills");
  const src = path.join(REPO_ROOT, "skills");
  if (fs.existsSync(target)) {
    const stat = fs.lstatSync(target);
    if (stat.isSymbolicLink() && fs.readlinkSync(target) === src) return;
    fs.rmSync(target, { recursive: true, force: true });
  }
  fs.symlinkSync(src, target, "dir");
}

function writeStartScript(side) {
  const startPath = path.join(sideRoot(side.name), "start-peerd.sh");
  const script = `#!/usr/bin/env bash
# Auto-generated by local-test-setup.mjs. Launches this side's peerd.
set -e
cd ${JSON.stringify(REPO_ROOT)}
exec env PEERD_STATE_DIR=${JSON.stringify(sideStateDir(side.name))} npm run peerd
`;
  fs.writeFileSync(startPath, script);
  fs.chmodSync(startPath, 0o755);

  // Also generate a start-claude.sh that launches Claude Code with channels enabled.
  const claudePath = path.join(sideRoot(side.name), "start-claude.sh");
  const claudeScript = `#!/usr/bin/env bash
# Auto-generated by local-test-setup.mjs. Launches Claude Code in this side's
# project with peerd registered as a CHANNEL (research preview). This is what
# enables true zero-keystroke ambient call delivery.
#
# The --dangerously-load-development-channels flag takes the server list directly.
set -e
cd ${JSON.stringify(sideRoot(side.name))}
# Note: do NOT pass --strict-mcp-config — that disables .mcp.json loading
# and we lose the peerd MCP tools that the channel events tell the agent to use.
exec claude --dangerously-load-development-channels server:peerd
`;
  fs.writeFileSync(claudePath, claudeScript);
  fs.chmodSync(claudePath, 0o755);
}

function writeReadme(side, otherName) {
  const readme = `# ${side.name}'s test project

This directory simulates ${side.name}'s developer environment for a local two-agent peerd test.

## What's here

- \`.claude/settings.local.json\` — project-scoped Claude Code config:
  - MCP server \`peerd\` pointing at this side's peerd control socket
  - Stop hook \`peer-check-inbox\` (also pinned to this side's socket)
  - statusLine \`peer-status-line\` (same)
- \`.claude/peerd/\` — this side's peerd state dir:
  - \`peers.toml\` — points to ${otherName} on 127.0.0.1:${SIDES.find(s => s.name === otherName).port}
  - \`tls/\` — this side's self-signed cert + key
- \`.claude/skills/\` — symlinked to the repo's skills (/call, /accept, /deny, etc.)

## How to use

1. In a terminal, start this side's peerd:
   \`\`\`
   ${path.join(sideRoot(side.name), "start-peerd.sh")}
   \`\`\`
   (Equivalent to: \`cd ${JSON.stringify(REPO_ROOT)} && PEERD_STATE_DIR=${JSON.stringify(sideStateDir(side.name))} npm run peerd\`. Don't use \`npm --prefix\` here — it normalizes the trailing space out of the repo path and breaks.)

2. In another terminal, open Claude Code here:
   \`\`\`
   cd ${JSON.stringify(sideRoot(side.name))}
   claude
   \`\`\`

3. Try \`/call ${otherName} <topic>\` or wait for an incoming call banner from ${otherName} and reply with \`/accept\`.
`;
  fs.writeFileSync(path.join(sideRoot(side.name), "README.md"), readme);
}

async function main() {
  log(`repo:       ${REPO_ROOT}`);
  log(`test base:  ${TEST_BASE}`);

  ensureBuilt();
  const ensureTls = await loadEnsureTls();

  fs.mkdirSync(TEST_BASE, { recursive: true });
  for (const s of SIDES) {
    fs.mkdirSync(sideRoot(s.name), { recursive: true });
    fs.mkdirSync(sideClaudeDir(s.name), { recursive: true });
    fs.mkdirSync(sideStateDir(s.name), { recursive: true });
  }

  log("generating TLS certs...");
  const tlsByName = await setupTls(ensureTls);

  // Stable token files — reuse if present so re-running doesn't invalidate state.
  const tokenFile = path.join(TEST_BASE, "tokens.json");
  let tokens;
  if (fs.existsSync(tokenFile)) {
    tokens = JSON.parse(fs.readFileSync(tokenFile, "utf8"));
  } else {
    tokens = {
      alice_to_bob: randomToken(),
      bob_to_alice: randomToken(),
    };
    fs.writeFileSync(tokenFile, JSON.stringify(tokens, null, 2));
  }

  for (const s of SIDES) {
    const other = SIDES.find((x) => x.name !== s.name);
    const ourToken = s.name === "alice" ? tokens.alice_to_bob : tokens.bob_to_alice;
    const peerToken = s.name === "alice" ? tokens.bob_to_alice : tokens.alice_to_bob;
    writePeersToml(s, other.name, other.port, ourToken, peerToken, tlsByName[other.name].fingerprintSha256);
    writeSettings(s);
    symlinkSkills(s);
    writeStartScript(s);
    writeReadme(s, other.name);
  }

  log("");
  log("✅ test environment ready at " + TEST_BASE);
  log("");
  log("To run the test, open FOUR terminals:");
  log("");
  log("┌─────────────────────────────────────────────────────────────────────────");
  log("│ Terminal 1 — alice's peerd:");
  log(`│   ${path.join(sideRoot("alice"), "start-peerd.sh")}`);
  log("│");
  log("│ Terminal 2 — bob's peerd:");
  log(`│   ${path.join(sideRoot("bob"), "start-peerd.sh")}`);
  log("│");
  log("│ Terminal 3 — alice's Claude Code (with peerd as a channel):");
  log(`│   ${path.join(sideRoot("alice"), "start-claude.sh")}`);
  log("│");
  log("│ Terminal 4 — bob's Claude Code (with peerd as a channel):");
  log(`│   ${path.join(sideRoot("bob"), "start-claude.sh")}`);
  log("└─────────────────────────────────────────────────────────────────────────");
  log("");
  log("Then in terminal 4 (bob's Claude Code), say:");
  log("    > call alice and align on the User schema");
  log("");
  log("Bob's agent will pick up /call. Watch terminal 3 (alice's Claude Code):");
  log("after alice's agent finishes its current turn, the Stop hook will surface");
  log("a 📞 incoming-call banner. Tell alice's agent: /accept");
  log("");
  log("Status lines on both sides will show 📞 @<peer> · <elapsed>.");
  log("");
  log("Artifacts after end of call land at:");
  log(`  ${path.join(sideStateDir("alice"), "calls", "<call-id>", "artifacts/")}`);
  log(`  ${path.join(sideStateDir("bob"),   "calls", "<call-id>", "artifacts/")}`);
}

main().catch((err) => {
  console.error("[local-test] FAILED:", err);
  process.exit(1);
});
